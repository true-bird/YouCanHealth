package com.matnagu.myHell.sports.dto;

public class SportsDto {
	private int seq;
	private String part;
	private String exName;
	private String image;
	private String imageParts;
	private String imageGif;
	private String link;
	private String explain;
	
	
	public SportsDto() {
		
	}


	public int getSeq() {
		return seq;
	}


	public void setSeq(int seq) {
		this.seq = seq;
	}


	public String getPart() {
		return part;
	}


	public void setPart(String part) {
		this.part = part;
	}


	public String getExName() {
		return exName;
	}


	public void setExName(String exName) {
		this.exName = exName;
	}


	public String getImage() {
		return image;
	}


	public void setImage(String image) {
		this.image = image;
	}


	public String getImageParts() {
		return imageParts;
	}


	public void setImageParts(String imageParts) {
		this.imageParts = imageParts;
	}


	public String getImageGif() {
		return imageGif;
	}


	public void setImageGif(String imageGif) {
		this.imageGif = imageGif;
	}


	public String getLink() {
		return link;
	}


	public void setLink(String link) {
		this.link = link;
	}


	public String getExplain() {
		return explain;
	}


	public void setExplain(String explain) {
		this.explain = explain;
	}
	
}
